-- ADMINISTRADOR: todos los permisos
DECLARE v_id NUMBER;
BEGIN
    SELECT ID_PERFIL INTO v_id FROM PERFIL WHERE NOMBRE_PERFIL = 'ADMINISTRADOR';
    FOR p IN (SELECT ID_PERMISO FROM PERMISO) LOOP
        INSERT INTO PERFIL_PERMISO VALUES (v_id, p.ID_PERMISO);
    END LOOP;
END;
/
-- SUPERVISOR: gestion academica + periodos + reportes
DECLARE v_id NUMBER;
BEGIN
    SELECT ID_PERFIL INTO v_id FROM PERFIL WHERE NOMBRE_PERFIL = 'SUPERVISOR';
    FOR p IN (SELECT ID_PERMISO FROM PERMISO WHERE NOMBRE_OPERACION IN (
        'GESTIONAR_PROGRAMAS','GESTIONAR_ASIGNATURAS','GESTIONAR_PERIODOS',
        'GESTIONAR_ESTUDIANTES','GESTIONAR_REGLAS','GESTIONAR_CODIGOS','VER_REPORTES','CONSULTAR_PAGOS')) LOOP
        INSERT INTO PERFIL_PERMISO VALUES (v_id, p.ID_PERMISO);
    END LOOP;
END;
/
-- ASISTENTE: operaciones financieras diarias
DECLARE v_id NUMBER;
BEGIN
    SELECT ID_PERFIL INTO v_id FROM PERFIL WHERE NOMBRE_PERFIL = 'ASISTENTE';
    FOR p IN (SELECT ID_PERMISO FROM PERMISO WHERE NOMBRE_OPERACION IN (
        'GENERAR_COBRO_IND','COBROS_ADICIONALES','GENERAR_COBRO_MAS','REGISTRAR_PAGO','VER_CUENTA')) LOOP
        INSERT INTO PERFIL_PERMISO VALUES (v_id, p.ID_PERMISO);
    END LOOP;
END;
