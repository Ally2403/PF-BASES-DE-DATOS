PROMPT  4. ESTADOS DE CUENTA (calculado desde movimientos)
PROMPT ============================================
SELECT
    sp.ESTUDIANTE,
    sp.NOMBRE_PERIODO,
    sp.TOTAL_COBROS,
    sp.TOTAL_PAGOS,
    sp.SALDO_NETO,
    CASE
        WHEN sp.SALDO_NETO <= 0                          THEN 'PAGADO'
        WHEN sp.TOTAL_PAGOS > 0 AND sp.SALDO_NETO > 0   THEN 'PARCIAL'
        ELSE                                                  'PENDIENTE'
    END AS ESTADO
FROM VW_SALDO_PERIODO sp
ORDER BY ESTADO, sp.ESTUDIANTE;

